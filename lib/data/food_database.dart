  /// ML Kit 라벨을 받아 가장 근접한 FoodData 반환
  static FoodData? findByLabel(String label) {
    final key = label.toLowerCase().replaceAll('_', ' ').trim();

    // 완전 일치
    if (_db.containsKey(key)) return _db[key];

    // 부분 일치 (길이 4자 이상의 키만)
    for (final entry in _db.entries) {
      if (entry.key.length >= 4) {
        if (key.contains(entry.key) || entry.key.contains(key)) {
          return entry.value;
        }
      }
    }
    return null;
  }

